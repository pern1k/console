package com.pern1k.console;

public class Person {
    String name;
    String surname;
    boolean male;
    int age;
    String[] inventory;



    public Person(String name, String surname, boolean male, int age, String [] inventory) {
        this.name = name;
        this.surname = surname;
        this.male = male;
        this.age = age;
        this.inventory = inventory;
    }


}
